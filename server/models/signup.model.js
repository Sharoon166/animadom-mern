import mongoose from "mongoose";
const signupSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        trim: true,
        unique: true
    },
    password: {
        type: String,
        required: true,
        trim: true
    },
   
}, {
    timestamps: true
});
export default mongoose.model('SignUp', signupSchema);
